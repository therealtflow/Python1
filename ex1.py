


grade = int(input('Enter a grade: '))

if grade < 101 and grade > 89 : print('A')
if grade < 90 and grade > 79 : print('B')
if grade < 80 and grade > 69 : print('C')
if grade < 70 and grade > 59 : print('D')
if grade < 60 and grade > -1 : print('F')
if grade > 101 or grade < 0 : print('not a valid input') 
